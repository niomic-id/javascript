// IF ELSE Dengan beberapa Kondisi

function mahasiswa() {
  var nilai = 61;

  if (nilai <= 60) {
    console.log("Tidak Lulus");
  } else if (nilai <= 50) {
    console.log("Tidak Lulus");
  } else if (nilai <= 40) {
    console.log("Tidak Lulus");
  } else if (nilai <= 30) {
    console.log("Tidak Lulus");
  } else {
    console.log("Lulus");
  }
}

mahasiswa();

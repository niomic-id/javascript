var umurBudi = 21;
var umurAndi = 21;

console.log(" === ");
console.log(umurBudi === umurAndi); // Membandingkan dengan Tipe Datanya juga Bukan Cuma Isinya biasanya string dan number
console.log("");

console.log(" == ");
console.log(umurBudi === umurAndi); // Membandingkan Isinya Tanpa Tipe Datanya
console.log("");

console.log(" != "); // Tidak sama dengan
console.log(umurBudi != umurAndi); // 21 tidak sama dengan 21 hasilnya : false / salah
console.log("");

console.log(" !== "); // Strict sama dengan
console.log(umurBudi !== umurAndi); // 21 strict tidak sama dengan 21 hasilnya : false / salah
console.log("");

console.log(" > "); // Lebih dari
console.log(umurBudi > umurAndi); // 21 lebih dari 21 hasilnya : false / salah
console.log("");

console.log(" < "); // Lebih dari
console.log(umurBudi < umurAndi); // UmurBudi=21 kurang dari 21 hasilnya : false / salah
console.log("");

console.log(" >= "); // Lebih Besar sama dengan
console.log(umurBudi >= umurAndi); // umurBudi=21 Lebih besar sama dengan 21 hasilnya : true / benar
console.log("");

console.log(" <= "); // Lebih Kecil sama dengan
console.log(umurBudi >= umurAndi); // umurBudi=21 Lebih Kecil sama dengan 21 hasilnya : true / benar
console.log("");

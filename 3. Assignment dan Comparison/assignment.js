/* 
Assignment / operator Penugasan
*/

var angka = 1;
var angka2 = 2;

console.log("1 + 2");
console.log((angka += angka2)); // += Artinya : angka=1 + angka2=2
console.log("========");

console.log("1 - 2");
console.log((angka -= angka2)); // -= Artinya : angka=1 - angka2=2
console.log("========");

console.log("1 * 2");
console.log((angka *= angka2)); // *= Artinya : angka=1 * angka2=2
console.log("========");

console.log("1 / 2");
console.log((angka /= angka2)); // /= Artinya : angka=1 / angka2=2
console.log("========");

console.log("1 modulus 2");
console.log((angka %= angka2)); // %= Artinya : angka=1 % angka2=2
